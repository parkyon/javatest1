package calculater;



public class calculater {


	
	
	
	public int sum (int i, int j) {
		
		int Sum = i + j;
		
		return Sum;
	}
	public int bbagi (int i, int j) {
		int Bbagi = i - j;
		return Bbagi;
		
	}
	public int gob(int i, int j) {
		int Gob = i * j;
		return Gob;
		
	}
	public double nanugi(int i, int j) {
		int Nanugi = i / j;
		return Nanugi;
	}
	public int mod(int i, int j) {
	int Mod = i % j;
	return Mod;
	}

}